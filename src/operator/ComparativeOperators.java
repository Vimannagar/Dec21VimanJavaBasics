package operator;

public class ComparativeOperators {
	
	public static void main(String[] args) {
		
//		greater than '>'
		
		int i = 100;
		int j = 20;
		
		boolean u = i>j;
		
		System.out.println(u);//true
		
//		less than '<'
		
	boolean	v = i<j;
	
	System.out.println(v);// false
	
	
//	equals to operator '=='
	
	boolean h = i==j;
	
	System.out.println(h);//false
	
	boolean w = i==100;
	System.out.println(w);// true
	
	

	
		
	}

}
