package operator;

public class LogicalOperator {
	
	
	public static void main(String[] args) {
//		AND logical operator
		
		int i = 10;
		
		int j = 50;
		
		boolean k = i==10;
		
	boolean	l = j<20;
	
		boolean m = k && l;
		
		System.out.println(m);//false
		
//		OR logical operator
		
	boolean	n = k || l;
	
	System.out.println(n);// true
		
		
		
		
	}

}
