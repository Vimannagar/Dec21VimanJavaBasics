package loopsinjava;

public class ForLoop {
	
	
//	syntax:
//			1				2, 5, 8				4, 7
//	for(any_java_statement; booleancondition; any_java_statement)
//	{	3, 6, 9
//		Actions
//	
//	}
	
	public static void main(String[] args) {
		
		int i=5;
		for(System.out.println("first statement"); i<10; System.out.println("second statement"))
		{
			
			System.out.println(i);
			i++;
			
		}
		
		
		
		for(int j=0; j<=10; j++)
		{
			
			System.out.print(j);
			
		}
		
		
	}

	
	
	

}
