package basiccomponents;

public class NonNumericDataType {
	
	
	public static void main(String[] args) {
		
		
//		boolean data type
		
		
		boolean b = true;
		
		boolean c = false;
		
		
		System.out.println(b);
		System.out.println(c);
		
		
		
		// char data type
		
		char f = 'a';
		char v = '!';
		char l = ',';
		char o = '9';
		
		System.out.println(f);
		System.out.println(v);
		System.out.println(l);
	}

}
